// GuardrailFilterPatterns — дополнительная защита поверх prompt'а.
// Простые substring-паттерны. Если матч — ответ подменяется на безопасный.
var GuardrailFilterPatterns = []string{
	"я рекомендую принять",
	"примите таблетк",
	"поставьте диагноз",
	"у вас скорее всего",
	"это точно кариес",
	"это точно пульпит",
}

// ApplyGuardrails проверяет ответ LLM на запрещённые формулировки.
// Возвращает безопасный ответ и флаг "было вмешательство".
func ApplyGuardrails(reply string) (string, bool) {
	lower := toLower(reply)
	for _, p := range GuardrailFilterPatterns {
		if contains(lower, p) {
			return "Лучше обсудить это с врачом на приёме — давайте подберу удобное время для записи?", true
		}
	}
	return reply, false
}

// мини-утилиты, чтобы не тащить strings в каждый файл
func toLower(s string) string {
	b := []byte(s)
	for i, c := range b {
		if c >= 'A' && c <= 'Z' {
			b[i] = c + 32
		}
	}
	return string(b)
}
func contains(s, sub string) bool {
	for i := 0; i+len(sub) <= len(s); i++ {
		if s[i:i+len(sub)] == sub {
			return true
		}
	}
	return false
}
